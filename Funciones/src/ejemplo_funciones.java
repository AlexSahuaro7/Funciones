public class ejemplo_funciones {
        public static void saludar(){
            System.out.println("hola");
        }
    public static void main(String[] args) {

            saludar();
            saludar();
    }
}
//public static → se usa para que la función pueda ser usada directamente desde main.
//
//void → significa que no devuelve ningún resultado.
//
//saludar() → es el nombre de la función.
//
//Dentro tiene un System.out.println() que muestra "¡Hola!".